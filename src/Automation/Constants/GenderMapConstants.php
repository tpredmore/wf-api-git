    /**
     * Get gender by first name
     *
     * @param string $firstName
     * @return string Gender code (M, F, or U)
     */
    public static function getGenderByName(string $firstName): string
    {
        $firstNameLower = strtolower(trim($firstName));

        return self::GENDER_NAME_MAP[$firstNameLower] ?? self::GENDER_UNKNOWN;
    }

    /**
     * Check if a name is mapped
     *
     * @param string $firstName
     * @return bool
     */
    public static function isNameMapped(string $firstName): bool
    {
        $firstNameLower = strtolower(trim($firstName));

        return isset(self::GENDER_NAME_MAP[$firstNameLower]);
    }
